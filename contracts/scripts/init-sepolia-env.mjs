#!/usr/bin/env node
/**
 * Create contracts/.env with a new deployer wallet if missing.
 * Fund the printed address via Base Sepolia faucet, then run:
 *   npm run preflight:sepolia
 *   npm run deploy:base-sepolia
 */
import { writeFileSync, existsSync, readFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { Wallet } from "ethers";

const __dirname = dirname(fileURLToPath(import.meta.url));
const envPath = join(__dirname, "..", ".env");

if (existsSync(envPath)) {
  const content = readFileSync(envPath, "utf8");
  if (content.includes("DEPLOYER_PRIVATE_KEY=0x") && !content.includes("DEPLOYER_PRIVATE_KEY=0x...")) {
    console.log(".env already exists with DEPLOYER_PRIVATE_KEY set.");
    console.log("Run: npm run preflight:sepolia");
    process.exit(0);
  }
}

const wallet = Wallet.createRandom();
const producer = Wallet.createRandom();
const auditor = Wallet.createRandom();

const env = `# Auto-generated by init-sepolia-env.mjs — DO NOT COMMIT
DEPLOYER_PRIVATE_KEY=${wallet.privateKey}
PRODUCER_ADDRESS=${producer.address}
AUDITOR_ADDRESS=${auditor.address}
BASE_SEPOLIA_RPC_URL=https://sepolia.base.org
`;

writeFileSync(envPath, env, { mode: 0o600 });

console.log("Created contracts/.env with new wallets.\n");
console.log("DEPLOYER (fund this address):", wallet.address);
console.log("PRODUCER:", producer.address);
console.log("AUDITOR:", auditor.address);
console.log("\n1. Fund deployer at https://www.alchemy.com/faucets/base-sepolia");
console.log("2. Run: npm run preflight:sepolia");
console.log("3. Run: npm run deploy:base-sepolia");
console.log("\nWARNING: Save these keys securely. .env is gitignored.");
